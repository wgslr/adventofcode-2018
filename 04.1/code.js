function cb(acc, x) {
  if ("id" in x) {
    acc.curr = x.id
    return acc
  } else if (x.text == "falls asleep") {
    acc.start = x.minute
    return acc
  } else if (x.text == "wakes up") {
    const end = x.minute;
    let { start, curr, guards, totals } = acc;

    if (guards[curr] === undefined) {
      guards[curr] = []
      totals[curr] = 0
      for (let i = 0; i < 60; ++i) {
        guards[curr][i] = 0
      }
    }

    for (let i = start; i < end; ++i) {
      guards[curr][i] += 1;
      totals[curr] += 1;
    }

    return acc
  }
}

function findMax(arr) {
  return arr.reduce((acc, x, idx) => {
    if (x > acc.max) {
      return { max: x, maxIdx: idx };
    } else {
      return acc;
    }
  }, { max: 0, maxIdx: -1 })
}

